#Tecnica utilizada para retornar uma sbString informando o inicio, fim e passo.

nome = "Jefferson Anderson Simplicio Silva"

print(nome[0]) # Primeira letra da String

print(nome[:9]) # Retorna até a posição 9 da string

print(nome[10:]) # Retorna da posiçao 10 em diante

print(nome[10:16]) # Retorna da posição 10 até 16

print(nome[10:16:2]) # Retorna da posição 10 até 16 pulando de 2 em 2

print(nome[:]) # Retorna toda a String

print(nome[::-1]) # Espelha a String