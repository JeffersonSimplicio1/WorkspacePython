#Operadores Logicos
    #Operador E (and)

saldo = 1000
saque = 200
limite = 100

verificacao = saldo >= saque and saque <= limite
print(verificacao)

    # Operador OU (or)

saldo = 1000
saque = 200
limite = 100

verificacao = saldo >= saque or saque <= limite
print(verificacao)

    # Operador de Negação

verificacao = not 1000 > 1500
print(verificacao)



