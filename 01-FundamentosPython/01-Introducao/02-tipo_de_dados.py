# Introdução aos tipos de dados em Python

#Comentarios são feitos com a cerquilha (#) no início da linha

# Tipos de dados básicos em Python:
# Inteiros (int) 
# Float (float)
# Booleanos (bool)
# Strings (str)

# Exemplos:

idade = 25               # int
altura = 1.75           # float
estudante = True        # bool
nome = "João"          # str
print("Idade:", idade)
print("Altura:", altura)
print("Estudante:", estudante)
print("Nome:", nome)

# Podemos verificar o tipo de dado usando a função type()
print("Tipo de idade:", type(idade))
print("Tipo de altura:", type(altura))
print("Tipo de estudante:", type(estudante))
print("Tipo de nome:", type(nome))

# Operações com diferentes tipos de dados
soma_inteiros = 10 + 5
soma_floats = 2.5 + 3.5
concat_strings = "Olá, " + "mundo!"
print("Soma de inteiros:", soma_inteiros)
print("Soma de floats:", soma_floats)
print("Concatenação de strings:", concat_strings)

# Conversão de tipos
idade_str = str(idade)
altura_int = int(altura)
print("Idade como string:", idade_str)
print("Altura como inteiro:", altura_int)
print("Tipo de idade_str:", type(idade_str))
print("Tipo de altura_int:", type(altura_int))
