# Operadores de Atribuição
  #Atribuição Simples: apenas atribui um valor a variavel (sinal =)

saldo = 500

print(saldo)

  #Atribuição com Adição: forma reduzida de somar o valor da variavel com um novo valor. Utiliza-se +=

saldo = 200
saldo += 300

print(saldo)

#Atribuição com Subtração:
saldo = 200
saldo -= 100

print(saldo)

#Atribuição com Multiplicação:
saldo = 200
saldo *= 2

print(saldo)

#Atribuição com Divisão (Float):
saldo = 200
saldo /= 2

print(saldo)

#Atribuição com Divisão int:
saldo = 200
saldo //=2

print(saldo)

#Atribuição com Modulo (Resto da divisão):
saldo = 200
saldo %= 3

print(saldo)

#Atribuição com Exponenciação:
saldo = 200
saldo **= 2

print(saldo)
